$(document).ready(function(){
	$('#hide').click(function(){
		$('#sente').hide();
	});
	$('#show').click(function(){
		$('#sente').show();
	});
	$('#csshide').click(function(){
		$('#csssen').css('display','none');

	});
	$('#cssshow').click(function(){
		$('#csssen').css('display','block');

	});

});

